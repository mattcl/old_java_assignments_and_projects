package data;

public class BCounter {

	protected int count;
	
	public BCounter() {
		count = 1;
	}
	
	/**
	 * Returns the count
	 * @return
	 */
	public int getCount() {
		return count;
	}
	
	/**
	 * Sets the count
	 * @param count
	 */
	public void setCount(int count) {
		this.count = count;
	}
}
